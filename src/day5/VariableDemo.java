package day5;

public class VariableDemo {

	public static void main(String[] args) {

		// variable declaration
		int a;
		// variable initialization
		a = 5;
		// variable declaration and initialization
		int b = 4;

		// multiple variable declaration
		int x, y, z;

		// multiple initialization
		x = 5;
		y = 6;
		z = 8;

		int i = 4, j = 7, k = 9;

		int m, n, o;
		m = n = o = 7;

		int d = 6;
		int e = d;
		
	}

}
