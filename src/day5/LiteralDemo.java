package day5;

public class LiteralDemo {

	public static void main(String[] args) {

		// 1. Integral Literal 4334,54675,767868768 (int)
		byte b = 127;
		short s = 32767;
		int i = 2147483647;
		long l1 = 2147483647;
		long l2 = 2147483648L;

		// 2.Floating Literal 343.56 233.45 (double
		float f = 34.56F;
		double d = 34.34;

		// 3. Boolean Literal
		boolean b1 = true;
		boolean b2 = false;

		// 4. Character Literal
		char c1 = '?';
		char c2 = '2';
		char c3 = 'a';
		char c4 = '\u0001';
		char c5 = 65;

		// 5. String Literal

		String s1 = "a";
		String s2 = "Nepal";
		String s3 = "abc@123#$12";
		String s4 = "Hello,Good Morning";
		String s5 = " ";
		String s6 = "";
		String s7 = "123";
		String s8 = "23.45";
		String s9 = "2020-01-11";

		// 6.Null Literal null
		String s10 = null;
	}

}
