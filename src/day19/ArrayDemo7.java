package day19;

import java.util.Scanner;

public class ArrayDemo7 {

	public static void main(String[] args) {
		int[][] arr = {{23, 34, 56},{45, 65, 67},{99, 88, 77}};

		
		for (int i = 0; i < arr.length; i++) {
			for (int j = 0; j < arr.length; j++) {
				System.out.print(arr[i][j]+ " ");
			}
			System.out.println();

	}

}
}
